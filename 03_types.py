'''
Tipos de datos primitivos
    Integer: Números enteros
    Float: Números decimales
    String: Cadena de caracteres
    Boolean: Verdadero o Falso
Tipos de datos 
    texto: str
    Numérico: int, float, complex
    Secuencias: list, tuple, range
    Mapeo: dict
    Set Types: set, frozenset
    Booleanos: bool
    Binarios: bytes, bytearray, memoryview

'''

# string
my_name = "Nombre 1"
my_name = 'Nombre 2'
my_name = "12"
print('my_name =>', my_name)
print(type(my_name))

# int
my_age = 12
print('my_age =>', my_age)
print(type(my_age))

# boolean
is_single = False
print('is_single =>', is_single)
print(type(is_single))

# inputs
my_age = input('¿Cuál es tu edad? ')
print('my_age =>', my_age)
print(type(my_age))